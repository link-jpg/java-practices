package PDK;

public class Constante {
    public static void main(String[] args) {
        /* Declaracion de constante */
        final int valor = 50;
        /*
         * Se utiliza la palabra reservada final seguido del tipo mas nombre
         * y dato asignado
         */
        System.out.println("Valor almacenado en la variable: " + valor);
    }
}
