public class Pvariables2 {
    public static void main(String[] args) {
        /*
         * Distintas maneras de asignar un valor a una variable
         */
        // Version 1
        byte valor; // Se escribe el tipo de dato y el nombre de la variable
        valor = 10; // se escribe la variable mas el dato que queremos asignarle
        System.out.println("Dato almacenado en valor: " + valor);// + sirve para concatenar
        // Fin version 1
        // Version 2
        byte valor1 = 10;// El tipo de dato mas el nombre mas = el valor a asignarle
        System.out.println("Dato almacenado en valor1: " + valor1);
        // Fin version 2

    }
}
